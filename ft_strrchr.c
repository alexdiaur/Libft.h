#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	d;

	d = c;
	i = ft_strlen((char *)s) + 1;
	while (i--)
	{
		if (*(s + i) == d)
			return ((char *)(s + i));
	}
	return (0);
}